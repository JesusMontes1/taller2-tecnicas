    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        // TODO add your handling code here:
        if (panelDibujo1.consultarNombres(jTextField1.getText())) {
            int eleccion = JOptionPane.showConfirmDialog(null, "Este nombre ya esta asignado en memoria. Â¿Sobreescribirlo?", "Advertencia", JOptionPane.YES_NO_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE);
            
            if (eleccion == JOptionPane.YES_OPTION) {
                panelDibujo1.setIndexActual(panelDibujo1.obtenerIndexNombres(jTextField1.getText()));
                panelDibujo1.sobreescribir(panelDibujo1.getIndexActual());
                jDialog1.setVisible(false);
            }
        }
        else {
            panelDibujo1.guardar(jTextField1.getText());
            JMenuItem temp = new JMenuItem();
            temp.setAction(new DibujosAction(panelDibujo1, ultimoIndex));
            temp.setText(jTextField1.getText());
            panelDibujo1.setIndexActual(ultimoIndex);
            jMenu4.add(temp);
            jTextField1.setText("");
            jDialog1.setVisible(false);
            ++ultimoIndex;
        }
    }//GEN-LAST:event_jButton16ActionPerformed

